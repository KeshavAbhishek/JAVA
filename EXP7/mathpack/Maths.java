package mathpack;

public class Maths {
    public static void findSquareroot(int n){
        System.out.println("Square root of " + n + " is = " + Math.sqrt(n));
    }

    public static void findSquare(int n){
        System.out.println("Square of " + n + " is = " + n * n);
    }

    public static void findCube(int n){
        System.out.println("Cube of " + n + " is = " + n*n*n);
    }
}
